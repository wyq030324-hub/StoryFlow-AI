奶奶没有说话，只是微笑着，张开的双臂没有放下。

小女孩低头看了看手里剩下的那些火柴，一根，两根，三根，还有五根。她怕火柴灭了奶奶就消失，一口气将全部剩下的火柴一齐划燃，捧在掌心，高高举起。

火光一下子大亮，比之前任何一次都要明亮，把那个小小的夹角照得如同白昼。砖墙上的每一道裂缝，地面上的每一粒沙石，都看得清清楚楚。

奶奶走过来，将她轻轻抱起。

那束火焰在她们两个人的周围燃烧，越来越亮，越来越高，将地面越推越远，将街道越推越远，将整座城市的灯光越推越远，最后消失在夜空深处，什么声音也没有留下。

第二天清晨，人们踏着霜走过那个街角，发现一个小女孩蜷缩在墙根下。她的脸颊上带着微笑，手里攥着一把烧尽的火柴梗，火柴头都已经烧成了灰，散在她的掌心里。

街上渐渐热闹起来，人们绕开她，继续走向各自要去的地方。

